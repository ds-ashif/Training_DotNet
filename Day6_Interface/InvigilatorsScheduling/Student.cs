namespace InvigilatorsScheduling
{
    /// <summary>
    /// Represents a student in the university.
    /// </summary>
    public class Student
    {
        /// <summary>
        /// Gets or sets student ID.
        /// </summary>
        public int StudentId { get; set; }

        /// <summary>
        /// Gets or sets student name.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Gets or sets student section.
        /// </summary>
        public string Section { get; set; }
    }
}
